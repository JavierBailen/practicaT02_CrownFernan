package Ejercicios.Relacion02_VariablesyTiposDatos_Jbg;

public class Ej03 {
    public static void main(String[] args) {
        int x = 144;
        int y = 999;

        System.out.println("La variable x = " + x);
        System.out.println("La variable y = " + y);
        System.out.println("La suma entre las dos variables (x+y) = " + (x + y));
        System.out.println("La resta entre las dos variables (x-y) = " + (x - y));
        System.out.println("La división entre las dos variables (x/y) = " + (double) x / y);
        System.out.println("La multiplicación entre las dos variables (x*y) = " + (x * y));
    }
}
